<h2>Program perjuangan — TANI MOTEKAR (Kongres II)</h2>
<p><strong>TANI MOTEKAR</strong> (Tanah, Infrastruktur, Modal, Teknologi, Akses Pasar) adalah program tuntutan dan platform perjuangan yang <strong>dicetuskan pada Kongres II, 10–11 Desember 2010</strong>, dari rekomendasi bahwa petani harus menguasai kelima pilar tersebut (merujuk ringkasan <a href="https://id.wikipedia.org/wiki/Serikat_Petani_Karawang" target="_blank" rel="noopener noreferrer">Wikipedia — Serikat Petani Karawang</a>, diakses 2026). Akronim ini menyatakan lima pilar yang harus dikuasai pekerja tani agar kemandirian dan kesejahteraan benar-benar berpijak pada kontrol produksi sendiri:</p>
<ol>
<li><strong>T — Tanah</strong>: pokok dasar perjuangan; alat produksi utama yang harus dikuasai dan didistribusikan secara adil kepada pekerja tani.</li>
<li><strong>I — Infrastruktur</strong>: jalan desa, irigasi teknis, dan fasilitas pasca-panen sebagai prasyarat produksi pertanian rakyat yang efisien.</li>
<li><strong>M — Modal</strong>: bukan sekadar uang, tetapi seluruh input pertanian — benih, pupuk, dan alat-mesin pertanian — yang harus berada dalam kontrol pekerja tani, bukan korporasi.</li>
<li><strong>T — Teknologi</strong>: teknologi tepat guna yang meningkatkan produktivitas tanpa membuat pekerja tani tergantung pada pasokan eksternal.</li>
<li><strong>A — Akses Pasar</strong>: rantai pemasaran produk pertanian dan olahan lanjutan yang memutus praktik tengkulak eksploitatif melalui model pertukaran antar-organisasi di basis massa.</li>
</ol>
<p><em>TANI MOTEKAR sebagai jalan menuju industrialisasi pertanian</em> — industri yang diselenggarakan di sektor pertanian pedesaan, berlandaskan partisipasi penuh masyarakat, ketersediaan bahan baku lokal, kelestarian lingkungan, dan pengabdian pada kepentingan publik. Dengan begitu, pengembangan industri di Karawang tidak lagi semata-mata berarti <em>konversi sawah menjadi pabrik</em>, melainkan penguatan produksi pertanian rakyat hingga hilirnya.</p>

