<h3>4. Dewan Pimpinan Tani Desa (DPTD)</h3>
<p>Struktur pimpinan di tingkat desa. Menurut Anggaran Rumah Tangga, DPTD dipilih dalam <strong>musyawarah desa</strong> untuk <strong>masa jabatan tiga tahun</strong> dan merupakan struktur tertinggi di desa. Komposisi umum: ketua, sekretaris, dan staf departemen. Rapat DPTD diatur secara berkala sesuai naskah ART.</p>
