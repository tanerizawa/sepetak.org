<h3>1. Kongres</h3>
<p>Forum tertinggi pembuat dan pengambil keputusan. Menurut Anggaran Rumah Tangga, kongres dilaksanakan <strong>sekurang-kurangnya tiga tahun sekali</strong>. Peserta terdiri dari seluruh jajaran pimpinan SEPETAK di setiap tingkat struktur dan anggota yang mendapat rekomendasi (dan persetujuan) pimpinan sesuai ketentuan yang berlaku.</p>

